//8. Antecessor e Sucessor: Receba um número inteiro e mostre quem vem antes e
//quem vem depois dele.



/*Numero inicial*/
let number = 10;

/*Definir sucessor e antecessor*/
const sucessor = number +1;
const antecessor = number -1;

/*REsultado*/
alert("O sucessor do numero ativo é "+ sucessor.toFixed(2)+ " E o antecesor é "+ antecessor.toFixed(2))